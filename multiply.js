/**
 * โปรแกรมสูตรคูณ (Multiplication Table)
 * ใช้ For Loop วนซ้ำคำสั่งเพื่อคำนวณและแสดงผลแม่สูตรคูณ
 */

// 1. กำหนดตัวแปรแม่สูตรคูณ (n)
let n = 2; 

console.log(`--- สูตรคูณแม่ ${n} ---`);

// 2. ใช้ For Loop วนตั้งแต่ i = 1 ถึง 12
// i++ คือการเพิ่มค่า i ทีละ 1 ในทุกๆ รอบที่จบการทำงาน
for (let i = 1; i <= 12; i++) {
    
    // 3. คำนวณผลลัพธ์ในแต่ละรอบ
    let result = n * i;
    
    // 4. แสดงผลลัพธ์ในรูปแบบ: n x i = result
    // ใช้ Template Literals (Backticks) เพื่อรวมข้อความกับตัวแปร
    console.log(`${n} x ${i} = ${result}`);
}

console.log(`---------------------`);

/**
 * อธิบายโค้ด:
 * - let i = 1: เริ่มต้นที่คูณ 1
 * - i <= 12: ทำซ้ำไปเรื่อยๆ ตราบใดที่ i ยังไม่เกิน 12
 * - i++: เมื่อจบรอบให้เพิ่มค่า i ขึ้นอีก 1 เพื่อไปคูณตัวถัดไป
 */